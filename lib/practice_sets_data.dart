// Static lookup map for stroke order data - built once at initialization
final Map<String, Map<String, String?>> _originalKanjiData = {};

// Initialize the lookup map with original data
void _initializeKanjiDataLookup() {
  if (_originalKanjiData.isEmpty) {
    for (var set in practiceSetsData.values) {
      for (var item in set.items) {
        if (!_originalKanjiData.containsKey(item.japanese)) {
          _originalKanjiData[item.japanese] = {
            'strokeOrder': item.strokeOrder,
            'kanjiVGCode': item.kanjiVGCode,
          };
        }
      }
    }
  }
}

// Lookup function to find strokeOrder and kanjiVGCode for any kanji character
Map<String, String?>? lookupKanjiData(String kanji) {
  _initializeKanjiDataLookup();
  return _originalKanjiData[kanji];
}

